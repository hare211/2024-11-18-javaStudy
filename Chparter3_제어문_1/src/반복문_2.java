
public class 반복문_2 {
	public static void main(String[] args) {
		// 1 ~ 10 까지 출력
		for (int i = 1; i <= 10; i++) {
			System.out.print(i + " ");
		}
		
		for (int i = 10; i >= 1; i--) {
			System.out.print(i + " ");
		}
		
		System.out.println();
		
		for (char i = 'A';  i <= 'Z' ; i++) {
			System.out.println(i);
		}
		for (char i = 'Z'; i >= 'A'; i--) {
			System.out.println(i);
			
		}
		for (char i = 'a'; i <= 'z'; i++) {
			System.out.println(i);
			
		}
	}

}
